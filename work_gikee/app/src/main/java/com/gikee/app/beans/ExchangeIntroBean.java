package com.gikee.app.beans;

public class ExchangeIntroBean {

    private String country;

    private String country_cn;

    private String intro;

    private String intro_cn;

    private String website;


    public String getCountry() {
        return country;
    }

    public void setCountry(String country) {
        this.country = country;
    }

    public String getCountry_cn() {
        return country_cn;
    }

    public void setCountry_cn(String country_cn) {
        this.country_cn = country_cn;
    }

    public String getIntro() {
        return intro;
    }

    public void setIntro(String intro) {
        this.intro = intro;
    }

    public String getIntro_cn() {
        return intro_cn;
    }

    public void setIntro_cn(String intro_cn) {
        this.intro_cn = intro_cn;
    }

    public String getWebsite() {
        return website;
    }

    public void setWebsite(String website) {
        this.website = website;
    }
}
