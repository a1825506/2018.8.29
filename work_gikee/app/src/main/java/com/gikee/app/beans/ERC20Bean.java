package com.gikee.app.beans;

public class ERC20Bean {

    private String block;

    private String direct;

    private String erc20_title;

    private String erc20_token;

    private String from;

    private String logo;

    private String symbol;

    private String to;

    private String tradehash;

    private String tradetime;

    private String value;


    public String getBlock() {
        return block;
    }

    public void setBlock(String block) {
        this.block = block;
    }

    public String getDirect() {
        return direct;
    }

    public void setDirect(String direct) {
        this.direct = direct;
    }

    public String getErc20_title() {
        return erc20_title;
    }

    public void setErc20_title(String erc20_title) {
        this.erc20_title = erc20_title;
    }

    public String getErc20_token() {
        return erc20_token;
    }

    public void setErc20_token(String erc20_token) {
        this.erc20_token = erc20_token;
    }

    public String getFrom() {
        return from;
    }

    public void setFrom(String from) {
        this.from = from;
    }

    public String getLogo() {
        return logo;
    }

    public void setLogo(String logo) {
        this.logo = logo;
    }

    public String getSymbol() {
        return symbol;
    }

    public void setSymbol(String symbol) {
        this.symbol = symbol;
    }

    public String getTo() {
        return to;
    }

    public void setTo(String to) {
        this.to = to;
    }

    public String getTradehash() {
        return tradehash;
    }

    public void setTradehash(String tradehash) {
        this.tradehash = tradehash;
    }

    public String getTradetime() {
        return tradetime;
    }

    public void setTradetime(String tradetime) {
        this.tradetime = tradetime;
    }

    public String getValue() {
        return value;
    }

    public void setValue(String value) {
        this.value = value;
    }
}
