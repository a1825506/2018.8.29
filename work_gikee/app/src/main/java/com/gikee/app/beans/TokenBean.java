package com.gikee.app.beans;

public class TokenBean {

    private String token;

    private String token_from;

    private String token_to;

    private String token_value;


    public String getToken() {
        return token;
    }

    public void setToken(String token) {
        this.token = token;
    }

    public String getToken_from() {
        return token_from;
    }

    public void setToken_from(String token_from) {
        this.token_from = token_from;
    }

    public String getToken_to() {
        return token_to;
    }

    public void setToken_to(String token_to) {
        this.token_to = token_to;
    }

    public String getToken_value() {
        return token_value;
    }

    public void setToken_value(String token_value) {
        this.token_value = token_value;
    }
}
